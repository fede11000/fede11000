       <h2>
       Fundación del ejercito:
    </h2>

 El 29 de mayo de 1810, la Primera
Junta de Gobierno dio nacimiento formal al Ejército Argentino al reconocer el
trabajo de las tropas militares durante el proceso revolucionario, elevó a
Regimientos a los batallones existentes y reorganizó las unidades dispuestas en
todo el territorio.
 En dicha proclama, la Primera Junta exaltó la actuación que las tropas militares
habían tenido en esa semana de mayo tan trascendental para la historia
argentina. Fueron pasos fundamentales para la futura independencia y la
formación del Ejército.
 28 de mayo de 1810, se había gestado el Departamento de Gobierno y Guerra,
cuyo secretario fue Mariano Moreno. De inmediato, se expidió un Bando de la
Junta que ordenó la entrega de armas de cualquier tipo en poder de los
particulares, con el propósito de asegurar el armamento de una fuerza orgánica.
 Con estas medidas, que institucionalizaron las fuerzas existentes, se dieron los
primeros pasos hacia la formación del Ejército Patriota que luego comenzaría el
camino hacia la Independencia, declarada seis años después.

Durante la colonia, como defensa contra las pretensiones portuguesas o británicas,
se había constituido elRegimiento Fijo de Infantería de Buenos Aires. La mayoría de
sus soldados eran criollos y su desempeño era bastante mediocre. Carecían de
equipamiento, instrucción y disciplina. Sus oficiales, mayormente españoles,
estaban relajados y no tenían conocimientos de táctica o estrategia militar.
Guarnecían las fortalezas del Virreinato -Buenos Aires, Ensenada, San Miguel, Santa
Tecla y Santa Teresa-. En caballería, se destacaban los Blandengues, milicias criollas
que guardaban las fronteras contra el indio y el portugués. Los había en Buenos
Aires, Santa Fe y Montevideo. José Gervasio Artigas, Estanislao López y José
Rondeau se iniciaron como "blandengues". Al principio se los armó con lanzas; pero
luego, el Virrey Vértiz los proveyó de sables, pistolas y carabinas. Su nombre se
debía al modo en que los soldados "blandían" sus lanzas, al saludar a las
autoridades, cuando eran revistados. El Real Cuerpo de Artillería era casi
inexistente. De los 200 efectivos, sólo 40 guardaban el fuerte porteño. El resto se
hallaban en la Banda Oriental.

