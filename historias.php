    <td>
    <h2>Sello conmemorativo de los Blandengues de Artigas</h2>
Después del fracaso del Regimiento "Fijo" en 1806, cuando su inacción permitió
que sólo 1.600 efectivos británicos tomaran una ciudad de más de 40.000 almas,
casi sin luchar; el comandante General de Armas, Santiago de Liniers y Bremond
decidió reforzar los cuerpos coloniales, para resistir un nuevo intento inglés. Así,
este francés convocó al pueblo de Buenos Aires, el 6 de Setiembre de 1806, a
enrolarse en diversos cuerpos, en razón del origen de cada recluta. Sería el germen
del futuro Ejército Argentino.
"Uno de los deberes más sagrados del hombre es la defensa de la Patria que le
alimenta, – decía Liniers - y los habitantes de Buenos Aires han dado siempre
pruebas de que conocen y saben cumplir con exactitud esta preciosa obligación".
Su llamado tuvo una gran acogida. Los hijos de Buenos Aires debían incorporarse
al Cuerpo de "Patricios"; los nacidos en las Provincias del Norte, en el de
"Arribeños"; los negros, mestizos, libertos e indios, en el Cuerpo de "Castas", o de
"Pardos y Morenos". Los españoles debían conformar sus propios batallones,
llamados "Tercios". Así se constituyeron los Tercios de: "Gallegos", "Andaluces",
"Montañeses", "Cántabros" (formados por vizcaínos y asturianos).
La caballería no era numerosa. No cualquiera tenía caballo. Los oficiales usaban el
suyo. Juan Martín de Pueyrredón, al constituir sus "Húsares del Rey", contribuyó a
vestirlos y montarlos, pues había hecho fortuna en el comercio. Destacaron los
"Migueletes", "Cazadores", "Carabineros" y "Quinteros" (jinetes de los arrabales).
    </td>
</tr>
<tr>
    <td>
    <h2>Santiago de Liniers, organizador de losprimeros cuerpos de un Ejército criollo</h2>
La artillería seguía escasa y rudimentaria, a cargo de los "Patriotas de la Unión"
(agrupaba a españoles y criollos) y de los "Pardos y Morenos". Era la menos
prestigiosa. No resultaba atractivo arrastrar pesados cañones, cargarlos, y llenarse
de pólvora, humo y metralla, o recibir disparos, sin poder defenderse, por servir al
cañón. Se desconocían los avances de la artillería francesa. El mismo Napoleón
Bonaparte era general de artillería. Los artilleros napoleónicos, orgullosos, decían
que su mejor defensa era "el humo de sus cañones". Faltaba aún para que la
artillería argentina adquiriera la importancia que la hizo destacar en Ituzaingó, al
mando del general Tomás de Iriarte.
Este nuevo ejército tenía más de 7.800 efectivos, y se empezó a entrenar de
inmediato. Los cuerpos debían concurrir en días fijos al Fuerte, "a fin de arreglar los
batallones y compañías, nombrando a los comandantes, y sus segundos, los
capitanes y sus tenientes, a voluntad de los mismos cuerpos". Era una novedad que
la tropa eligiera sus propios jefes y oficiales; sin requerirse, tampoco, instrucción
alguna. Esto se apartaba de las Ordenanzas Militares españolas, pero ante la
inminencia de un nuevo ataque inglés y el prestigio de Liniers, nadie se opuso. El
cuerpo más numeroso era la "Legión de Patricios Voluntarios Urbanos", como se
llamaba oficialmente, que conformó tres batallones. Le seguían el de Castas y los
Arribeños. Los vistosos uniformes del ejército, armas, pólvora y nuevas obras de
defensa se costearon con donativos, suscripciones y préstamos.
    </td>
</tr>
<tr>
    <td>
    <h2>Dragón y fusilero del Regimiento Fijo de Buenos Aires</h2>
El flamante ejército realizaba maniobras, a las que el público concurría y aplaudía.
Martín Rodríguez, de Húsares, diría, no sin cierta exageración: "Puede asegurarse
que a los tres meses después de la creación de estos Cuerpos, podían ellos competir
con las mejores tropas de Europa en su disciplina y maniobras". Manuel Belgrano,
de Patricios, disentía: "Ni la disciplina ni la subordinación era lo que debía ser";
agregando que la tropa "decía con mucha gracia que, para defender el suelo patrio
no había necesitado aprender a hacer posturas ni figuras en las plazas públicas para
diversión de las mujeres ociosas".
    </td>
</tr>
<tr>
    <td>
    <h2>La Segunda Invasión inglesa fue la prueba de fuego del flamante
ejército patrio</h2>
La prueba de fuego del flamante ejército tuvo lugar durante la Segunda Invasión
Inglesa. Allí, con mucho coraje y sin tanta técnica asombró a los propios británicos:
"Esta gente no es la raza afeminada que hay en España: al contrario, son feroces y
sólo necesitan disciplina para hacerlos formidables". El mismo Ministro de Guerra
Británico declaró ante el Parlamento: "El mérito de nuestros soldados fue
aumentado, en mucho, por la valerosa defensa efectuada por los contrarios. Del
mismo modo en que esta poderosa resistencia exalta la gloria de la conquista,
abrigo la esperanza de que el valor demostrado por las tropas españolas inspirará
a sus compatriotas en Europa a mostrar un espíritu parecido para resistir al
enemigo común". Este discurso se pronunció tras la invasión napoleónica a España;
donde Inglaterra pasaba a ser aliada contra los franceses. Durante la "Defensa" de
Buenos Aires, este flamante e improvisado ejército, junto al pueblo de la
ciudad,conducidos por Liniers, batieron a más de 9.000 soldados veteranos
profesionales, despejando, para siempre, su amenaza de conquista.
    </td>
</tr>
<tr>
    <td>
    <h2>Las segundas invasiones fueron la prueba de fuego para el
naciente ejército criollo</h2>
Liniers fue ascendido, primero a Mariscal de Campo, y luego, a Virrey del Río de la
Plata, el 3 de diciembre de 1807. Los criollos tomaron consciencia de su fortaleza y
su capacidad de defenderse; que en los momentos de dificultad, poco o nada se
podía esperar de la Metrópoli. Se perdió la antipatía hacia las milicias; y éstas
comenzaron a acercarse a quienes motorizaban las ideas de independencia.
Mientras aumentaba la autoconfianza en los criollos, crecía la antipatía hacia las
fuerzas coloniales españolas. Sobre ellas, el propio Cabildo manifestaba: "¿Qué
podía esperarse de unos Jefes que, en lo menos que han pensado toda su vida ha
sido en arreglar sus regimientos y en sujetarlos a la disciplina?. La verdad es que
jamás hemos visto una parada, y así han ido todas las cosas del servicio. ¿Qué se
podía esperar de los oficiales subalternos, que a excepción de uno y otro muy raros,
los demás han hecho su carrera en el pasatiempo, el juego, el baile, el paseo, sin
contraerse aún por momentos a nada de lo concerniente al servicio? ¿Qué
podíamos, por fin, esperar de unos hombres que tienen tanto esmero en sus
regimientos, que el Fijo de Infantería sólo cuenta hoy 72 soldados de servicio, y
para éstos hay 94 oficiales; que el de Dragones cuenta con otros tantos soldados
que aquél, poco más o menos, y mayor número de oficiales, sucediendo lo mismo
con el de Blandengues?".
Luego vino el previsible choque entre los cuerpos españoles y los criollos. Cornelio
Saavedra admitía que, a los españoles, "acostumbrados a mirar a los hijos del país
como sus dependientes, y tratarlos con el aire de conquistadores, les era
desagradable verlos con las armas en la mano". El conflicto se precipitó durante el
Virreinato de Liniers. El no ser éste español, y haber creado a los cuerpos criollos,
a quienes trataba con consideración, lo hizo un virrey muy popular entre éstos;
pese a que su gestión como gobernante dejara bastante que desear. Como
contrapartida, se fue ganando paulatinamente la desconfianza y el recelo de los
españoles. Agudizó esta crisis la invasión napoleónica a España: con lo que Francia
pasó a ser enemiga de los españoles. Éstos buscaban, entonces, la manera de
deponer a Liniers. Los conspiradores se agruparon en torno a don Martín de Alzaga,
alcalde de Primer Voto de Buenos Aires. El Cabildo fue el centro de la
confabulación. De la conjura participaron: el Obispo Lué, Mariano Moreno (a quien
nunca le cayó bien Liniers) y los "Tercios" españoles de gallegos, Vizcaínos
(cántabros) y catalanes. También participó el 3º Batallón de los Patricios. El 1º de
enero de 1809 coparon la Plaza de la Victoria, al grito de: "¡Muera el francés
Liniers!", "¡Junta como en España!", vivando al Cabildo.
