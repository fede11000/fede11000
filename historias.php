<tr>
    <td>
    <h2>Soldados del Regimiento de Patricios</h2>
Alzaga y Moreno llegaron al Fuerte a exigir la renuncia del virrey. Éste, acorralado,
alcanzó a firmarla. En ese momento, irrumpió Saavedra con los jefes de las tropas
leales a Liniers: Arribeños, Húsares, Patriotas de la Unión, junto a los Tercios de
Montañeses y Andaluces. Le manifestaron su apoyo al virrey, y le obligaron a
romper su renuncia. Seguidamente, intimaron a los sublevados a retirarse. Bastó
una breve carga de los Húsares de Martín Rodríguez y que salieran los cañones de
los Patriotas de la Unión a la plaza, para concluir el motín.
Esta asonada mostró a los futuros líderes de la Primera Junta (Saavedra y Moreno)
en bandos antagónicos: ya entonces no coincidían políticamente, y seguramente
se tenían antipatía. Además, hubo dos "Tercios" españoles que sostuvieron al
virrey: los Andaluces y los Montañeses; pues muchos de sus miembros eran
criollos. Otra sorpresa fue que los "Patriotas de la Unión", cuerpo creado y
sostenido por el Cabildo, se enfrentó a su propia Institución madre. Dos batallones
de la Legión de Patricios permanecieron leales al virrey y el Tercer Batallón
(influenciado por Mariano Moreno) acompañó a los sediciosos.
Agradecido, Liniers reconoció que "la energía y el patriotismo de los Cuerpos y Jefes
ya citados me sacaron de este conflicto con el mayor denuedo". Saavedra dijo: "así
concluyó aquel día memorable... porque, en efecto, en él las armas de los hijos de
Buenos Aires abatieron el orgullo y miras ambiciosas de los españoles y adquirieron
superioridad sobre ellos". Liniers disolvió a los "Tercios" sublevados: Vizcaínos,
Gallegos y Catalanes. Sólo se salvaron los Andaluces y Montañeses. A aquéllos se
les quitaron sus banderas y se les prohibió usar uniforme. Se destituyó al Jefe del
3º Batallón de Patricios, y se desterró a los responsables de la conjura; despejando
el horizonte de eventuales oponentes a fuerzas mayormente criollas.
    </td>
</tr>
<tr>
    <td>
    <h2>Uniforme de los Patricios</h2>
El panorama se complicó con el arribo de Baltasar Hidalgo de Cisneros, en
reemplazo de Liniers. A su llegada, las tropas no lo aclamaron, y se lo recibió de
mala gana. El nuevo virrey indultó a los responsables del 1º de Enero, y devolvió
sus banderas a los oficiales de los Tercios disueltos; pero sin volverlos a constituir;
dejándolos como "reserva", como "Batallones del Comercio". Por razones
económicas eliminó varias unidades menores. Redujo a 2 los batallones de Patricios
(que eran 3). Puso a sueldo sólo a los oficiales en actividad y suprimió 2 escuadrones
de los Húsares.
Finalmente, y "para evitar las rivalidades que suelen introducir la nominación", les
quitó los nombres que tenían, hasta entonces, las unidades de Infantería, y las pasó
a numerar, como simples "batallones". Así: 1 y 2 correspondían a los dos batallones
subsistentes de Patricios; 3 a los Arribeños; 4 a los Montañeses, 5 a los Andaluces,
6 a la reserva de los Cuerpos Urbanos del Comercio, 7 a los Granaderos de Fernando
VII y 8 a Pardos y Morenos. Así fue cómo el último virrey del Río de la Plata les dio
a los Patricios el número que hasta el día de hoy tienen, como Regimiento de
Infantería de Línea Nº 1. Sin embargo, todo el mundo siguió llamando a las
unidades con sus denominaciones tradicionales. Estas reformas le granjearon la
antipatía del ejército que, de ser "mimado" con Liniers, pasaba a sufrir el "ajuste"
de Cisneros, quien además les quitaba las denominaciones con las que
orgullosamente habían expulsado al invasor inglés, y a reivindicar a los "Tercios"
españoles disueltos. Por eso, el ejército, resentido con el virrey, respaldó
decisivamente las acciones de mayo.
La Primera Junta aprendió la lección y le dio un gran impulso al ejército. El 27 de
mayo, cuenta Juan Beruti, "todas las tropas de Artillería, Infantería y Caballería
formaron un cuadro en la plaza; salió la Junta, el presidente las arengó, y juraron
obediencia; y luego hicieron una descarga de artillería y fusilería, con lo cual se
concluyó". Dos días después, el 29, a instancias del secretario de Guerra y Gobierno,
Mariano Moreno, la Junta emitió una proclama, considerada el nacimiento formal
del Ejército Argentino, por la cual reconocía el protagonismo de las tropas durante
la gesta del 25 de mayo y ordenaba varias medidas para aumentar "la fuerza militar
de estas Provincias".
Elevó todos los Batallones de Infantería a Regimientos (al revés de lo que había
hecho Cisneros), con 1.116 efectivos cada uno. Ordenó reincorporar a los que
habían sido dados de baja, "que actualmente no estuvieron ejerciendo algún arte
mecánico o servicio público" y dispuso una leva de "todos los vagos y hombres sin
ocupación", entre 18 y 40 años. El vocal Miguel de Azcuénaga tenía a su cargo la
"Armería Real", que entregaba fusiles a cada cuerpo, en función del número de
soldados. Se obligó a los vecinos a depositar en casa de Azcuénaga sus armas y
mandó pagar sueldo a todos los soldados alistados.
La Revolución sabía que se iniciaba un arduo camino hacia la Independencia; que
iba a costar mucho sacrificio, lucha, sinsabores y sangre. Por eso se preparaba para
una pelea que sabía terrible, de la mano de un ejército que había vencido a los
ingleses y había contribuido decisivamente a terminar con el Virreinato del Río de
la Plata. Así nacía, formalmente, el Ejército Argentino.
    </td>
