    <td>
    En la actualidad, la rama terrestre de las Fuerzas Armadas se ocupa de contribuir a la
defensa nacional para proteger nuestra independencia y soberanía. Además, quienes
lo integran están a cargo de la protección de los recursos naturales, el medio
ambiente y la integridad territorial, así como de contribuir con el desarrollo científico,
tecnológico, económico y social, cooperar para el logro del bienestar general de las
comunidades, participar en misiones de paz y asistencia humanitaria, y prestar apoyo
en la lucha contra el narcoterrorismo. En el contexto de emergencia por el COVID-19,
el Ejército participa de la activación de 14 comandos conjuntos de zonas de
emergencia y 10 Fuerzas de Tarea en todo el país, desarrollando acciones de
protección civil en ayuda humanitaria.
